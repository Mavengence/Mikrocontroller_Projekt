void check_response (void)
{
	DWT_Delay_us (40);
	if (!(HAL_GPIO_ReadPin (GPIOB, GPIO_PIN_11)))
	{
		DWT_Delay_us (80);
		if ((HAL_GPIO_ReadPin (GPIOB, GPIO_PIN_11))) check = 1;
	}
	while ((HAL_GPIO_ReadPin (GPIOB, GPIO_PIN_11)));   // wait for the pin to go low
}

uint8_t read_data (void)
{
	uint8_t i,j;
	for (j=0;j<8;j++)
	{
		while (!(HAL_GPIO_ReadPin (GPIOB, GPIO_PIN_11)));   // wait for the pin to go high

		DWT_Delay_us (40);   // wait for 40 us
		if ((HAL_GPIO_ReadPin (GPIOB, GPIO_PIN_11)) == 0)   // if the pin is low 
		{
			i&= ~(1<<(7-j));   // write 0
		}
		else i|= (1<<(7-j));  // if the pin is high, write 1
		while ((HAL_GPIO_ReadPin (GPIOB, GPIO_PIN_11)));  // wait for the pin to go low
	}
	return i;
}

